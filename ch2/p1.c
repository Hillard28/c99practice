#include <stdio.h>

int main(void)
{
  //Can execute as multiple printf statements (easier to read)
  printf("       *\n");
  printf("      *\n");
  printf("     *\n");
  printf("*   *\n");
  printf(" * *\n");
  printf("  *\n\n");
  
  //Or as one confusing one
  printf("       *\n      *\n     *\n*   *\n * *\n  *");
  
  return 0;
}
