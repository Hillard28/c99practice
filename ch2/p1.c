#include <stdio.h>

int main(void)
{
  //Can execute as multiple printf statements (easier to read)
  printf("       *");
  printf("      *");
  printf("     *");
  printf("*   *");
  printf(" * *");
  printf("  *");
  
  //Or as one confusing one
  printf("       *\n      *\n     *\n*   *\n * *\n  *");
  
  return 0;
}
